public class Arithmetics {
    public void doArithmetics(int a, int b) {
        System.out.printf("Сумма: %d%n", a + b);
        System.out.printf("Разность: %d%n", a - b);
        System.out.printf("Произведение: %d%n", a * b);
        System.out.printf("Частное: %d%n", a / b);
        System.out.printf("Остаток: %d%n", a % b);
        System.out.printf("Максимальное: %d%n", Math.max(a, b));
    }
}
